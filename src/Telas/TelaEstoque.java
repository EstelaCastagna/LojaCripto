    public void preencherTabela(){
        
        
          for (int i = 0; i < 40; i++) {
            
            TabelaEstoque.setValueAt("", i, 0);
            TabelaEstoque.setValueAt("", i, 1);
            TabelaEstoque.setValueAt("", i, 2);
            TabelaEstoque.setValueAt("", i, 3);
            TabelaEstoque.setValueAt("", i, 4);
          }
        for (int i = 0; i < constEstoque.size(); i++) {
            TabelaEstoque.setValueAt(constEstoque.get(i).getId_modelo(),i,0);
            TabelaEstoque.setValueAt(constEstoque.get(i).getNome_marca(),i,1);
            TabelaEstoque.setValueAt(constEstoque.get(i).getNome_modelo(),i,2);
            TabelaEstoque.setValueAt(constEstoque.get(i).getVersao_modelo(),i,3);
            TabelaEstoque.setValueAt(constEstoque.get(i).getQuantidade(),i,4);
        }
  
    }

